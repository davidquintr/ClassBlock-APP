    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(tfCalifmax.getText().isBlank() || tfCalifmin.getText().isBlank() || tfTitulo.getText().isBlank())
                throw new java.lang.NumberFormatException();
            else{
                if(taDescripcion.getText().isBlank())
                    taDescripcion.setText("Sin descripción");
                
                calificacionMaxima = Float.parseFloat(tfCalifmax.getText());
                calificacionObtenida= Float.parseFloat(tfCalifmin.getText());
                descripcion = taDescripcion.getText();
                titulo = tfTitulo.getText();
                fechaDeEntrega = jdFecha.getCalendar();
                    if(cbEntrega.isSelected()){
                        estadoDeEntrega = "Entregado";
                    }else{
                        estadoDeEntrega = "No entregado";
                    }
                tipoDeActividad = tipoActividad.getSelectedItem().toString();
                int id = gui.app.getUsuario().getAsignaturas().get(posAsignatura).getActividades().get(posActividad).getIdActividad();
                gui.app.getUsuario().getAsignaturas().get(posAsignatura).editarActividad(calificacionObtenida, calificacionMaxima, descripcion, estadoDeEntrega, fechaDeEntrega, tipoDeActividad, titulo, id);
                JOptionPane.showMessageDialog(null, "Actividad editada con éxito", "Agregar Actividad",1);
                this.setVisible(false);
            }
            
            gui.actividades.forEach(actividad -> actividad.actualizar());
            
        }catch(java.lang.NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Compruebe sus datos!", "Agregar Actividad",0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jbEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEliminarActionPerformed
        int id = gui.app.getUsuario().getAsignaturas().get(posAsignatura).getActividades().get(posActividad).getIdActividad();
        if(JOptionPane.showConfirmDialog(null, "¿Desea eliminar esta actividad?\nEsta acción es permanente", "Eliminar Actividad",JOptionPane.OK_CANCEL_OPTION,JOptionPane.WARNING_MESSAGE)==JOptionPane.OK_OPTION){
            gui.app.getUsuario().getAsignaturas().get(posAsignatura).eliminarActividad(id);
            JOptionPane.showMessageDialog(null, "Asignatura eliminada", "Eliminar Actividad",JOptionPane.PLAIN_MESSAGE);
            this.setVisible(false);
            gui.actividades.get(posAsignatura).actualizar();
        }

    }//GEN-LAST:event_jbEliminarActionPerformed

