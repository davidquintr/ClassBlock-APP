    private void clearFields(){
        tfTitulo.setText("");
        taDescripcion.setText("");
        cbEntrega.setSelected(false);
        //jdcFechaEntrega.setDate(null);
        tipoActividad.setSelectedIndex(1);
        tfCalifmax.setText("");
        tfCalifmin.setText("");
    }
    
    public void colocarDatos(){
        tfTitulo.setText(this.titulo);
        taDescripcion.setText(this.descripcion);
        labelTitulo.setText(this.titulo +  ": editar");
        if(this.estadoDeEntrega.equals("Entregado")){
            cbEntrega.setSelected(true);
        }else{
            cbEntrega.setSelected(false);
        }
        
        jdFecha.setCalendar(fechaDeEntrega);
        if(tipoDeActividad.equals("Formativa")){
            tipoActividad.setSelectedIndex(1);
        }else{
            tipoActividad.setSelectedIndex(0);
        }
        tfCalifmax.setText(String.valueOf(calificacionMaxima));
        tfCalifmin.setText(String.valueOf(calificacionObtenida));
    }
    

