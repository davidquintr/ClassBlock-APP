    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            if(tfCalifmax.getText().isBlank() || tfCalifmin.getText().isBlank() || tfTitulo.getText().isBlank())
                throw new java.lang.NumberFormatException();
            else{
                boolean repetido = false;
                for (int i = 0; i < gui.app.getUsuario().getAsignaturas().get(position).getActividades().size(); i++) {
                    if(tfTitulo.getText().equals(gui.app.getUsuario().getAsignaturas().get(position).getActividades().get(i).getTitulo())){
                        repetido = true;
                        break;
                    }
                    repetido = false;
                 }
                if(!repetido){
                    if(taDescripcion.getText().isBlank())
                        taDescripcion.setText("Sin descripci칩n");

                    calificacionMaxima = Float.parseFloat(tfCalifmax.getText());
                    calificacionObtenida= Float.parseFloat(tfCalifmin.getText());
                    descripcion = taDescripcion.getText();
                    titulo = tfTitulo.getText();
                    fechaDeEntrega = jdFecha.getCalendar();
                        if(cbEntrega.isSelected()){
                            estadoDeEntrega = "Entregado";
                        }else{
                            estadoDeEntrega = "No entregado";
                        }
                    tipoDeActividad = tipoActividad.getSelectedItem().toString();
                    gui.app.getUsuario().getAsignaturas().get(this.position).agregarActividad(calificacionObtenida, calificacionMaxima, descripcion, estadoDeEntrega, fechaDeEntrega, tipoDeActividad, titulo);
                    JOptionPane.showMessageDialog(null, "Actividad creada con 칠xito", "Agregar Actividad",1);
                    gui.actividades.get(this.position).actualizar();
                    this.setVisible(false);
                    clearFields();
                }else{
                    JOptionPane.showMessageDialog(null,"춰No puedes agregar una actividad repetida!", "Agregar Actividad",0);
                }
            }
        }catch(java.lang.NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Compruebe sus datos!", "Agregar Actividad",0);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

